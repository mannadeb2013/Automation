package Inheritance;

public class Grandpa {
	
	void house() {
		System.out.println("Grandpa won 3 house");
		
	}
	void car() {
		System.out.println("He won 3 cars");
		
	}
	int motorcycle() {
		System.out.println("He won 1 motorcycle");
		return 0;
	}
	static void tree() {
		System.out.println("He won 3 tree");
		
	}
	

}
